	// alpha = cover for opaque source.
tail_have_alpha:
	MOVBLZX AX, R9
	MOVBLZX (DI), R10
	CMPQ R9, R10
	JAE tail_r_add
	SUBQ R9, R10
	MOVQ R10, R11
	IMULQ R8, R11
	ADDQ $128, R11
	MOVQ R11, R12
	SHRQ $8, R12
	ADDQ R12, R11
	SHRQ $8, R11
	MOVBLZX (DI), R10
	SUBQ R11, R10
	MOVB R10, (DI)
	JMP tail_g

