// func blendSolidHspanRGBAAVX2Asm(dst []byte, covers []byte, pixelOpaque uint32, srcA uint8, count int)
TEXT ·blendSolidHspanRGBAAVX2Asm(SB), NOSPLIT, $0-64
	MOVQ dst_base+0(FP), DI
	MOVQ covers_base+24(FP), SI
	MOVL pixelOpaque+48(FP), AX
	MOVBLZX srcA+52(FP), BX
	MOVQ count+56(FP), CX

	TESTQ CX, CX
	JLE done

	VPXOR X15, X15, X15
	VMOVD AX, X10
	PSHUFD $0, X10, X10
	VPMOVZXBW X10, Y10
	VMOVDQU ·bias128W(SB), Y14

	MOVL BX, DX
	CMPQ CX, $4
	JB tail

loop_dispatch:
	CMPQ DX, $255
	JE loop_opaque

	MOVD DX, X11
	VPBROADCASTW X11, Y11

loop_alpha:
	VMOVDQU (DI), X0
	MOVL (SI), R13
	VMOVD R13, X1
	VPSHUFB ·alphaDupMask4(SB), X1, X1
	VPMOVZXBW X0, Y0
	VPMOVZXBW X1, Y1
	VPMULLW Y1, Y11, Y2
	VPADDW Y14, Y2, Y2
	VPSRLW $8, Y2, Y3
	VPADDW Y3, Y2, Y2
	VPSRLW $8, Y2, Y2
	JMP blend_words

loop_opaque:
	VMOVDQU (DI), X0
	MOVL (SI), R13
	VMOVD R13, X1
	VPSHUFB ·alphaDupMask4(SB), X1, X1
	VPMOVZXBW X0, Y0
	VPMOVZXBW X1, Y2

blend_words:
	VPMAXUW Y10, Y0, Y3
	VPMINUW Y10, Y0, Y4
	VPSUBW Y4, Y3, Y5
	VPMULLW Y2, Y5, Y6
	VPADDW Y14, Y6, Y6
	VPSRLW $8, Y6, Y7
	VPADDW Y7, Y6, Y6
	VPSRLW $8, Y6, Y6
	VPADDW Y6, Y0, Y7
	VPSUBW Y6, Y0, Y8
	VPCMPEQW Y10, Y3, Y9
	VPAND Y9, Y7, Y7
	VPANDN Y8, Y9, Y8
	VPOR Y8, Y7, Y7
	VEXTRACTI128 $1, Y7, X8
	VPACKUSWB X8, X7, X7
	VMOVDQU X7, (DI)
	ADDQ $16, DI
	ADDQ $4, SI
	SUBQ $4, CX
	CMPQ CX, $4
	JGE loop_dispatch

tail:
	TESTQ CX, CX
	JLE done

tail_loop:
	MOVBLZX (SI), R8
	TESTQ R8, R8
	JZ tail_next

	CMPQ BX, $255
	JNE tail_alpha
	CMPQ R8, $255
	JNE tail_use_cover
	MOVL AX, (DI)
	JMP tail_next

