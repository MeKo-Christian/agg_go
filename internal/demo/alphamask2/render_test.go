package alphamask2

import "testing"

func TestNewClibcRandMatchesKnownSeedStates(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name string
		seed int32
		want [31]int32
	}{
		{
			name: "seed1",
			seed: 1,
			want: [31]int32{
				-1726662223, 379960547, 1735697613, 1040273694, 1313901226,
				1627687941, -179304937, -2073333483, 1780058412, -1989503057,
				-615974602, 344556628, 939512070, -1249116260, 1507946756,
				-812545463, 154635395, 1388815473, -1926676823, 525320961,
				-1009028674, 968117788, -123449607, 1284210865, 435012392,
				-2017506339, -911064859, -370259173, 1132637927, 1398500161, -205601318,
			},
		},
		{
			name: "seed1234",
			seed: 1234,
			want: [31]int32{
				997300753, 1787873760, -240326740, -39015925, -856741081,
				-2132388246, 1157487307, 1514271441, 112649172, -76012625,
				1994128572, 2062673662, 2076976597, 516503355, 1318736635,
				993161121, 888449716, 1552615853, -98235190, -1188648751,
				-140598225, -882898109, 538146087, 808667150, -1994922881,
				-1790577642, 716409717, -245274338, -1236232937, -501689970, 1965975355,
			},
		},
	}

	for _, tc := range tests {
		tc := tc
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()

			rng := newClibcRand(tc.seed)
			if rng.fptr != 3 || rng.rptr != 0 {
				t.Fatalf("seed %d pointers = (%d,%d), want (3,0)", tc.seed, rng.fptr, rng.rptr)
			}
			if rng.state != tc.want {
				t.Fatalf("seed %d state = %v, want %v", tc.seed, rng.state, tc.want)
			}
		})
	}
}
